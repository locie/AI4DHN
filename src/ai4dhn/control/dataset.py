from pathlib import Path
import json

import numpy as np
import pandas as pd
from sklearn.preprocessing import MinMaxScaler



class FromJSON(dict):
    """`dict` class overloading. The dict is initialized with data loaded from a JSON file. 
    """
    def __init__(self, path):
        with open(path, "r") as f:
            content = json.load(f)
        super().__init__(**content)

class _VariableData:
    """Utility class that loads data from the disk and preprocess it. 
    It must not be instanciated. Please instanciate a `Dataset` instance instead.

    This class is a container for time-dependent inputs and parameters needed in the simulation. 
    This data is loaded from CSV files generated by the scenarios generator. 

    Expected variables names are:
    
    - "Space heating [W]": space heating demand
    Becomes "Q_SH_ref" in the simulation space. 

    - "DHW draw-off [kg/s]": domestic hot water demand
    Becomes "m_DHW_w" in the simulation space.

    - "dni": direct irradiance [W/m2]
    Becomes "I_dir" in the simulation space.

    - "dhi": diffuse irradiance [W/m2]
    Becomes "I_diff" in the simulation space.

    - "temp_air": outdoor temperature [°C]
    Becomes "T_ext" in the simulation space.

    """

    # Defines lower and higher bounds for each variable
    # Required by the normalization process.
    # Defined bounds are valid for most of world climate conditions. Yet feel free to redefine it.
    # Technical note: having the same bounds for each studies scenario/architecture makes it easier to apply a given PPO model
    #   to unseen data.
    _variable_data_scaling_reference = pd.DataFrame(
                    {"Q_SH_ref": [0, 200000], 
                    "m_DHW_w": [0, 2],
                    "I_dir": [0, 1500],         
                    "I_diff": [0, 1500],      
                    "T_ext": [-40, 45],     
                    "hour_sin": [-1, 1],
                    "hour_cos": [-1, 1], 
                    "month_sin": [-1, 1],
                    "month_cos": [-1, 1],
                    "is_week_day": [0, 1],     
                    })
    
    
    def __init__(self, scenario_dir, SH_filter_mode, SH_filter_threshold, SH_lower_bound):
        self._load(scenario_dir=scenario_dir)
        self._apply_SH_lower_bound(SH_lower_bound)
        self._filter_on_SH(threshold=SH_filter_threshold, mode=SH_filter_mode)
        self._scale()
        self.variable_data = self.variable_data.reset_index(drop=True)

    def _load(self, scenario_dir):
        df = pd.read_csv(scenario_dir/"data.csv", index_col=0, parse_dates=True)
        if "index" in df.columns:
            raise NameError(f"File {scenario_dir/'data.csv'} has a column named \"index\", please choose another name for this column.")
        
        time_step = (df.index[1:] - df.index[:-1]).unique()
        if len(time_step) > 1:
            raise ValueError("Time step is not constant, please check your data.")
        else:
            self.time_step = int(time_step[0].total_seconds())
        
        # 1 from Monday to Friday, 0 from Saturday to Sunday
        is_week_day = np.where(df.index.dayofweek.isin(range(0,5)), 1, 0)

        # Hour is a circular information that could be difficult to understand by the PPO model
        # (indeed, hour following 23 (= 11 pm) is 0 (= 0 am))
        # This is simplified by converting hour to sinus and cosinus coordinates.
        # (same for month)
        polar_hour =  2 * np.pi / 24
        polar_month = 2 * np.pi / 12

        self.variable_data = pd.DataFrame({
                            "Q_SH_ref": df["Space heating [W]"], 
                            "m_DHW_w": df["DHW draw-off [kg/s]"],
                            "I_dir": df["dni"], 
                            "I_diff": df["dhi"],
                            "T_ext": df["temp_air"],
                            "hour_sin": np.sin(df.index.hour * polar_hour),
                            "hour_cos": np.cos(df.index.hour * polar_hour),
                            "is_week_day": is_week_day,     
                            "month_sin": np.sin((df.index.month - 1) * polar_month),
                            "month_cos": np.cos((df.index.month - 1) * polar_month),
                            "index": df.index
                            })

    def _scale(self):
        """Scale the data so that it falls in the `_variable_data_scaling_reference` bounds.

        Scaled data is concatenated (along axis=1) with unscaled data. Only the column names differ since
        a scaled variable is named "var_name (norm)".
        """
        data = self.variable_data
        cols_ref =  set(self._variable_data_scaling_reference)
        cols = set(data.select_dtypes(include="number"))
        for col in cols:
            if col not in cols_ref:
                raise IndexError(f"No min and max values exist for scaling data '{col}'.")
            if (data[col].min() < self._variable_data_scaling_reference[col].min()) or \
               (data[col].max() > self._variable_data_scaling_reference[col].max()) :
                raise ValueError(
f"""Data is out of bounds of predefined min and max values for scaling.
Please change these min/max or your data.

column {col} has (min, max) = ({data[col].min()}, {data[col].max()})
out of bounds ({self._variable_data_scaling_reference[col].min()}, {self._variable_data_scaling_reference[col].max()})
""")
        columns = list(cols & cols_ref)
        
        # todo old ref: (0, 1)
        scaler = MinMaxScaler(feature_range=(-1, 1))
        
        scaler = scaler.fit(self._variable_data_scaling_reference[columns])
        data_ = scaler.transform(data[columns])
        data_ = pd.DataFrame(data_, 
                            columns=[f"{c} (norm)" for c in columns],
                            index=data.index)
        self.variable_data = pd.concat([data, data_], axis=1)

    def _filter_on_SH(self, threshold: int, mode="summer"):
        """
        Remove all data samples where the space heating demand Q_SH_ref is smaller than `threshold`

        Parameters
        ----------
        data :
        threshold: float
        mode: ("summer", "multi-periods", "contiguous")
            If "summer": the removed period is the largest one where Q_SH_ref <= threshold
            If "multi-periods": all periods where Q_SH_ref <= threshold are removed. 
                The resulting data is highly fragmented.
            If "contiguous": only one contiguous period of data is removed. 
                This periods starts when Q_SH_ref <= threshold for the first time and ends
                when Q_SH_ref <= threshold for the last time (chronological order)

        Notes
        -----

        Modes "summer" and "contiguous" are based on the hypothesis that first and last values
        of Q_SH_ref are > threshold.

        Resulting data size depends on mode: "summer" > "multi-periods" > "contiguous"

        Returns
        -------
        data
            Sampled data
        """
        assert mode in ("summer", "multi-periods", "contiguous")
        data = self.variable_data
        if mode == "contiguous":
            cond_ = data["Q_SH_ref"] <= threshold
            if cond_.any():
                if cond_.all():
                    raise ValueError("Threshold too high, no remaining data")
                else:
                    non_zero, *_ = np.nonzero(cond_)
                    start, end = non_zero[[0,-1]]
                    cond = np.full(shape=data["Q_SH_ref"].shape, fill_value=True)
                    cond[start:end+1] = False
                    data = data[cond]
        elif mode == "multi-periods":
            data = data[data["Q_SH_ref"] > threshold]
        else: # mode == "summer"
            cond_ = data["Q_SH_ref"] <= threshold
            if cond_.any():
                if cond_.all():
                    raise ValueError("Threshold too high, no remaining data")
                else:
                    # those are index vales where `cond` is verified, i.e. values that would be drop in multi-periods mode
                    pos_idx = np.nonzero(cond_)[0]

                    # all groups of index values that satisfy `cond`
                    groups = []

                    # the current group being defined                
                    group = [pos_idx[0]]
                    for k in range(1, len(pos_idx)):
                        # `pos_idx[k]` is the index of a value that verifies cond

                        # `pos_idx[k-1]` is the index preceding `pos_idx[k-1]`, 
                        # meaning previous group can be extended
                        if pos_idx[k] == pos_idx[k-1] + 1:
                            group.append(pos_idx[k])

                        # `pos_idx[k-1]` is an index that was at least 2 positions before `pos_idx[k-1]`, 
                        # meaning previous group ended and a new one is defined, with first element `pos_idx[k]`
                        else:
                            groups.append(group)
                            group = [pos_idx[k]]
                    largest_group = sorted(groups, key=lambda x: len(x), reverse=True)[0]
                    idx1, *_, idx2 = largest_group
                    cond = np.full(shape=data["Q_SH_ref"].shape, fill_value=True)
                    cond[idx1:idx2+1] = False
                    data = data[cond]
        self.variable_data = data

    def _apply_SH_lower_bound(self, value):
        """Replace by a given value all space heating demands that are smaller than this value.

        This does not drop any values, all samples are kept. 
        This must not be confused with the `_filter_on_SH` selection of data.

        Notes
        -----
        Use of this method is intended to limit the amplitude of the space heating physical error.
        Indeed, this error uses "Q_SH_ref" as a denominator, leading to very large values whenever "Q_SH_ref" is close to zero.
        """
        self.variable_data["Q_SH_ref"] = np.maximum(self.variable_data["Q_SH_ref"], value) 

class Dataset(_VariableData):
    """Container for the data needed in the simulation. 


    - `variable_data`: pd.DataFrame
    Variable data created by the scenarios scripts. Must at least contains meteorological and thermal demand data.
    Provided as CSV file named "data.csv", in the `scenario_dir` scenario directory.
    Please see `python scenarios/merge.py --help` to produce this file.
    Please see also the _VariableData (abstract class) documentation.

    - `constant_data`: dict
    Relevant data for substation control. 
    Provided as JSON file named "constant_data.json", in the `scenario_dir` scenario directory.
    Example of file:
    ```
    {
    "value 1": 0.01,
    "value 2": 3452,
    }
    ```

    - `SST_parameters`: dict
    Parameters expected by the Simulink substation model. Typical use is to call the `setP_from_dict` method of Interface 
    instance to set these parameters before simulation starts.
    Provided as JSON file named "SST_parameters.json", in the `scenario_dir` scenario directory.

    """
    def __init__(self, scenario_dir, SH_filter_mode="multi-periods", SH_filter_threshold=-1, SH_lower_bound=10):
        """Load scenario data from disk and preprocess it. 

        Parameters
        ----------
        scenario_dir : pathlib.Path
            Path to a scenario directory. Must contains 3 files:

            - data.csv
            - constant_data.json
            - SST_parameters.json

            Please see Dataset class documentation for further information.
        SH_filter_mode : str, {"summer", "multi-periods", "contiguous"}
            Method for removing data samples with low SH demand. 
            Pass a negative `SH_filter_threshold` with `SH_filter_mode`="multi-periods" to skip this filtering step.

            If "summer": the removed period is the largest one where Q_SH_ref <= SH_filter_threshold
                With Q_SH_ref refering to the  "Space heating [W]" column in data.csv.
            If "multi-periods": all periods where Q_SH_ref <= SH_filter_threshold are removed. 
                The resulting data is highly fragmented.
            If "contiguous": only one contiguous period of data is removed. 
                This periods starts when Q_SH_ref <= SH_filter_threshold for the first time and ends
                when Q_SH_ref <= SH_filter_threshold for the last time (chronological order)

        SH_filter_threshold : float
            Threshold needed by the SH filtering.
        SH_lower_bound : float, optional
            Replace by a given value all space heating demands that are smaller than this value, by default 10
        """
        super().__init__(scenario_dir, SH_filter_mode, SH_filter_threshold, SH_lower_bound)
        self._scenario_dir = scenario_dir
        self.constant_data =  FromJSON(scenario_dir / "constant_data.json")
        self.SST_parameters =  FromJSON(scenario_dir / "SST_parameters.json")

    @property
    def scenario_dir(self):
        return self._scenario_dir